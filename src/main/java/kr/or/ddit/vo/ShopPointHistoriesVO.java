package kr.or.ddit.vo;

public class ShopPointHistoriesVO {
	private int  point_histories_no;
	private String  point_update_date;
	private int  point_status;
	private int  point_use_amount;
	private int  point_content;
	private String  mem_id;
	
	
	public int getPoint_histories_no() {
		return point_histories_no;
	}
	public void setPoint_histories_no(int point_histories_no) {
		this.point_histories_no = point_histories_no;
	}
	public String getPoint_update_date() {
		return point_update_date;
	}
	public void setPoint_update_date(String point_update_date) {
		this.point_update_date = point_update_date;
	}
	public int getPoint_status() {
		return point_status;
	}
	public void setPoint_status(int point_status) {
		this.point_status = point_status;
	}
	public int getPoint_use_amount() {
		return point_use_amount;
	}
	public void setPoint_use_amount(int point_use_amount) {
		this.point_use_amount = point_use_amount;
	}
	public int getPoint_content() {
		return point_content;
	}
	public void setPoint_content(int point_content) {
		this.point_content = point_content;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	
}
